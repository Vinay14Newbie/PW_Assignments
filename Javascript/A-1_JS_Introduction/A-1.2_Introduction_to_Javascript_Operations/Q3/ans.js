// Q3. Differentiate between unary, binary, and ternary operators in JavaScript. Give examples of each.


// Unary operator
// it's operate on single operand
let num = 10
console.log(-num)  //-10


// binary operator
// operates on two operands
let a = 5
let b = 2
console.log(a + b)  //7


// Ternary operator
// Operates on three operands
// It is also known as the conditional operator
let age = 19
let ans = (age > 19) ? "Adult" : "Teen"
console.log(ans)  //Teen